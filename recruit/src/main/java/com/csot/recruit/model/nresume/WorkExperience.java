package com.csot.recruit.model.nresume;

import java.io.Serializable;
import java.util.Date;

/**
 * 工作经历
 * @author honglu
 *
 */
public class WorkExperience implements Serializable {
  
  private static final long serialVersionUID = 1L;

  /** 工作业绩     */
  private String achievement;
  
  /** 工作地点    */
  private PropertyData location;  // email obj
  
  /** 雇主公司    */
  private String company;
  
  /** 雇主公司类型    */
  private PropertyData companyType; // email obj
  
  /** 起始时间    */
  private Date startingDate;
  
  /** 截止时间    */
  private Date endingDate;
  
  /** 雇主公司所属行业    */
  private PropertyData industry; // email obj
  
  /** 直接汇报对象    */
  private String directReportTo;
  
  /** 工作内容描述    */
  private String jobDescription;
  
  /** 职位名称    */
  private String jobTitle;
  
  /** 月薪    */
  private String monthlySalary;
  
  /** 直接下属人数    */
  private Integer numberOfSubordinates;

  public String getAchievement() {
    return achievement;
  }

  public void setAchievement(String achievement) {
    this.achievement = achievement;
  }

  public PropertyData getLocation() {
    return location;
  }

  public void setLocation(PropertyData location) {
    this.location = location;
  }

  public String getCompany() {
    return company;
  }

  public void setCompany(String company) {
    this.company = company;
  }

  public PropertyData getCompanyType() {
    return companyType;
  }

  public void setCompanyType(PropertyData companyType) {
    this.companyType = companyType;
  }

  public Date getStartingDate() {
    return startingDate;
  }

  public void setStartingDate(Date startingDate) {
    this.startingDate = startingDate;
  }

  public Date getEndingDate() {
    return endingDate;
  }

  public void setEndingDate(Date endingDate) {
    this.endingDate = endingDate;
  }

  public PropertyData getIndustry() {
    return industry;
  }

  public void setIndustry(PropertyData industry) {
    this.industry = industry;
  }

  public String getDirectReportTo() {
    return directReportTo;
  }

  public void setDirectReportTo(String directReportTo) {
    this.directReportTo = directReportTo;
  }

  public String getJobDescription() {
    return jobDescription;
  }

  public void setJobDescription(String jobDescription) {
    this.jobDescription = jobDescription;
  }

  public String getJobTitle() {
    return jobTitle;
  }

  public void setJobTitle(String jobTitle) {
    this.jobTitle = jobTitle;
  }

  public String getMonthlySalary() {
    return monthlySalary;
  }

  public void setMonthlySalary(String monthlySalary) {
    this.monthlySalary = monthlySalary;
  }

  public Integer getNumberOfSubordinates() {
    return numberOfSubordinates;
  }

  public void setNumberOfSubordinates(Integer numberOfSubordinates) {
    this.numberOfSubordinates = numberOfSubordinates;
  }
  
}
