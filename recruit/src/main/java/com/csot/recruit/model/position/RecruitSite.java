package com.csot.recruit.model.position;

import java.util.List;

/**
 * @author honglu
 * 招聘网站
 */
public class RecruitSite {
  
  // siteURL
  private String url;
  
  // site name
  private String siteName;
  
  // 验证码图片名
  private String captchaName;
  
  // 用户名输入值
  private String userNameInput;
  
  // 密码输入值
  private String pwdInput;
  
  // 验证码输入值
  private String captchaInput;
  
  // 招聘网站登录和分发职位需要的所有字段信息
  private List<RecruitSiteItem> rsItem;
  
  // 登录所需的额外字段
  private String rsiAccountStr;
  
  // 职位分发所需的额外字段
  private String rsiDistributeStr;
  
  // 职位分发的注意事项
  private String tips;
  
  // 发布的职位
  private PositionMana position;

  public String getUrl() {
    return url;
  }

  public void setUrl(String url) {
    this.url = url;
  }

  public String getSiteName() {
    return siteName;
  }

  public void setSiteName(String siteName) {
    this.siteName = siteName;
  }

  public String getCaptchaName() {
    return captchaName;
  }

  public void setCaptchaName(String captchaName) {
    this.captchaName = captchaName;
  }

  public String getUserNameInput() {
    return userNameInput;
  }

  public void setUserNameInput(String userNameInput) {
    this.userNameInput = userNameInput;
  }

  public String getPwdInput() {
    return pwdInput;
  }

  public void setPwdInput(String pwdInput) {
    this.pwdInput = pwdInput;
  }

  public String getCaptchaInput() {
    return captchaInput;
  }

  public void setCaptchaInput(String captchaInput) {
    this.captchaInput = captchaInput;
  }

  public List<RecruitSiteItem> getRsItem() {
    return rsItem;
  }

  public String getRsiAccountStr() {
    return rsiAccountStr;
  }

  public void setRsiAccount(String rsiAccountStr) {
    this.rsiAccountStr = rsiAccountStr;
  }

  public String getRsiDistributeStr() {
    return rsiDistributeStr;
  }

  public void setRsiDistribute(String rsiDistributeStr) {
    this.rsiDistributeStr = rsiDistributeStr;
  }

  public void setRsItem(List<RecruitSiteItem> rsItem) {
    this.rsItem = rsItem;
  }

  public String getTips() {
    return tips;
  }

  public void setTips(String tips) {
    this.tips = tips;
  }

  public PositionMana getPosition() {
    return position;
  }

  public void setPosition(PositionMana rPosition) {
    this.position = rPosition;
  }
}
