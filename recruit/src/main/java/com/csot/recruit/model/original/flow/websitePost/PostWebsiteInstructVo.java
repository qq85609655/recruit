package com.csot.recruit.model.original.flow.websitePost;

import java.io.Serializable;

public class PostWebsiteInstructVo extends PostWebsiteInstruct implements Serializable {
  public PostWebsiteInstructVo(PostWebsiteInstruct postWebsiteInstruct) {
    this.setId(postWebsiteInstruct.getId());
    this.setCreateDate(postWebsiteInstruct.getCreateDate());
    this.setCreatorId(postWebsiteInstruct.getCreatorId());
    this.setDeputyId(postWebsiteInstruct.getDeputyId());
    this.setDeputyName(postWebsiteInstruct.getDeputyName());
    this.setInstructType(postWebsiteInstruct.getInstructType());
    this.setModifiedId(postWebsiteInstruct.getModifiedId());
    this.setPostId(postWebsiteInstruct.getPostId());
    this.setPostName(postWebsiteInstruct.getPostName());
    this.setRecruitPositionId(postWebsiteInstruct.getRecruitPositionId());
    this.setAfterState(postWebsiteInstruct.getAfterState());
    this.setIsDeal(postWebsiteInstruct.getIsDeal());
    this.setUpdateDate(postWebsiteInstruct.getUpdateDate());
    this.setWebsitePostName(postWebsiteInstruct.getWebsitePostName());
    this.setWebsiteType(postWebsiteInstruct.getWebsiteType());
    this.setBeforeState(postWebsiteInstruct.getBeforeState());
    this.setPostWebsiteId(postWebsiteInstruct.getPostWebsiteId());
  }

  // 扩展属性 star
  private String instructTypeName; // 指令类型名称
  private String afterStateName; // 操作前状态
  private String websiteTypeName; // 发布渠道名称
  private String isDealName; // 指令类型名称
  private String instructDateName; // 指令时间
  private String beforeStateName; // 操作后状态

  // 扩展属性 end

  private static final long serialVersionUID = 1L;

  public String getInstructTypeName() {
    return instructTypeName;
  }

  public void setInstructTypeName(String instructTypeName) {
    this.instructTypeName = instructTypeName;
  }

  public String getAfterStateName() {
    return afterStateName;
  }

  public void setAfterStateName(String afterStateName) {
    this.afterStateName = afterStateName;
  }

  public String getWebsiteTypeName() {
    return websiteTypeName;
  }

  public void setWebsiteTypeName(String websiteTypeName) {
    this.websiteTypeName = websiteTypeName;
  }

  public String getInstructDateName() {
    return instructDateName;
  }

  public void setInstructDateName(String instructDateName) {
    this.instructDateName = instructDateName;
  }

  public String getIsDealName() {
    return isDealName;
  }

  public void setIsDealName(String isDealName) {
    this.isDealName = isDealName;
  }

  public String getBeforeStateName() {
    return beforeStateName;
  }

  public void setBeforeStateName(String beforeStateName) {
    this.beforeStateName = beforeStateName;
  }

}
