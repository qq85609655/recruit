package com.csot.recruit.model.nresume;

import java.io.Serializable;
import java.util.List;

/**
 * 邮件
 * @author honglu
 *
 */
public class EmailItem implements Serializable{

  private static final long serialVersionUID = 1L;

  /** 邮件主题    */
  private String subject;
  
  /** 来自     */
  private String from;
  
  /** 内容    */
  private String body;
  
  /** 邮件html视图    */
  private EmailFile alternativeView;
  
  /** 附件    */
  private List<EmailFile> attachments;
  
  /** 保存地址    */
  private String saveDir;

  public String getSubject() {
    return subject;
  }

  public void setSubject(String subject) {
    this.subject = subject;
  }

  public String getFrom() {
    return from;
  }

  public void setFrom(String from) {
    this.from = from;
  }

  public String getBody() {
    return body;
  }

  public void setBody(String body) {
    this.body = body;
  }

  public EmailFile getAlternativeView() {
    return alternativeView;
  }

  public void setAlternativeView(EmailFile alternativeView) {
    this.alternativeView = alternativeView;
  }

  public List<EmailFile> getAttachments() {
    return attachments;
  }

  public void setAttachments(List<EmailFile> attachments) {
    this.attachments = attachments;
  }

  public String getSaveDir() {
    return saveDir;
  }

  public void setSaveDir(String saveDir) {
    this.saveDir = saveDir;
  }
}
