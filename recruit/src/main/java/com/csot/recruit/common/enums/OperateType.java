package com.csot.recruit.common.enums;

public enum OperateType {
  // 功能日志
  OP("0"),
  
  // 操作记录
  OPRECORD("1"),
  
  // 评论
  COMMENT("2");
  
  private String value;
  
  private OperateType(String value) {
    this.setValue(value);
  }

  public String getValue() {
    return value;
  }

  public void setValue(String value) {
    this.value = value;
  }
}
