var positionCategory = {
	num : 0,
	selectedPositions : "",
	init : function() {
		// 点击出现职位大类  
		$("#choose-position-span").click(function(){
			if($(".position-category-pdata ul").length > 0) {
				$(".position-category-pdata").show();
			}
			else {
				var append = "<ul>";
				var lis = [];
				for(var i = 1; i < job.length; i++) {
					lis[i] = "<li><a datavalue=" + i + ">" + job[i].name  + "</a></li>";
				}
				append += lis.join("") + "</ul>";
				$(".position-category-pdata").append(append);
				$(".position-category-pdata").show();
			}
			$(".position-category-data").html("");
		});
		
		// 职位大类点击出现子职位
		$(document).delegate(".position-category-pdata a","click",function(){
			var d = [];
			var datavalue = $(this).attr("datavalue");
			var arrayList = job[datavalue].children;
			for(var i = 0; i < arrayList.length; i++){
				var p = arrayList[i]["Name"];
				var id = arrayList[i]["Id"];
				d[i] = '<li class="hasChild2"  onclick="positionCategory.chooseCategory(' + id + ",'"
				+ p + '\')" title="' + p + '">' +'<input type="checkbox" onclick="positionCategory.checkboxCategory(' + id + ",'"+ p + '\')" class=po_'+ id +'>'+ p +'</li>'
			}
			$(".position-category-pdata").hide();
			$(".position-category-data").html(d.join(""));
			positionCategory.setPositionDataSelect();
		});
	},
	setValue : function(d) { 
		if(d) {
			positionCategory.selectedPositions = d;
		}
		positionCategory.setSelectedPosition();
	},
	createPositionData : function(id, p) { // 生成一个职位子类
		return '<li class="hasChild" value="'+id+'" id=po_'+ id +' onclick="positionCategory.removeCategory(' + id + ",'"
		 + p + '\')" title="' + p + '">' +'<input type="checkbox" checked="checked" >'+ p +'</li>';
	},
	setSelectedPosition : function() { // 设置已选中的职位
		$(".position-category-pdata").hide();
		$(".position-category-data").html("");
		$(".choose-position-data").html("");
		var d = "";
		var arra = positionCategory.selectedPositions.split(":");
		if(arra.length > 1) {
			var arrb = arra[0].split(",");
			var arrc = arra[1].split(",");
			for(var i = 0; i < arrb.length; i++) {
				d += positionCategory.createPositionData(arrb[i],arrc[i]);
			}
		}
		$(".choose-position-data").append(d);
	},
	chooseCategory : function(id,p) { // 选中某个职位子类
	    var d = "";
		var c=".po_"+id;
		if(positionCategory.num < 5){
			if($(c).attr("checked")){
				$(c).attr("checked",false);
				$("#po_"+id).remove();
				positionCategory.num--;
			}else{
				$(c).attr("checked","checked");
				d += positionCategory.createPositionData(id,p)
				$(".choose-position-data").append(d);
				positionCategory.num++;
			}
		}else{
			if($(c).attr("checked")){
				$(".position-category-prompt").empty();
				$(c).attr("checked",false);
				$("#po_"+id).remove();
				positionCategory.num--;
			}else{
				$(c).removeAttr("checked"); 
				$(".position-category-prompt").html("<font color='red'>不能多于五个</font>");
			}
		}
	},
	checkboxCategory : function(id,p) { // 设置子类的选中状态
		var c=".po_"+id;
		if($(c).attr("checked")){
			$(c).attr("checked",false);
		}else{
			$(c).attr("checked",true);
		}
	},
	removeCategory : function(id,p) { // 移除某个选中的职位子类
		$("#po_"+id).remove();
		positionCategory.num--;
		$(".po_"+id).attr("checked",false);
	},
	setPositionDataSelect : function() {  // 切换不同的职位大类时,根据已选择的子类,设置子类集合中的选中状态
		$(".choose-position-data li").each(function() {
			var id = $(this).attr("id");
			$(".position-category-data li ." + id).attr("checked",true);
		});
	},
	submitCategoryData : function() { // 关闭时
		var name = "", value = "";
		var arrayLi = $(".choose-position-data li");
		for(var i = 0; i < arrayLi.length; i++) {
			name += arrayLi[i].title + ",";
			value += arrayLi[i].value + ",";
		}
		if(arrayLi.length > 0) {
			name = name.substring(0, name.length - 1);
			value = value.substring(0, value.length - 1);
			positionCategory.selectedPositions = value + ":" + name;
		}
		hideWindow("#position-category-win");
		positionCategory.closeAfter();
	},
	closeAfter: function() {} // 自定义关闭后函数
};