//快速查找框在失去焦点和获得焦点时所达到的效果
$("#searchIndustry").focus(function() {
	if ($.trim(this.value) == this.defaultValue) {
		this.value = "";
		$(this).removeClass("input-in");
	}
}).blur(function() {
	if ($.trim(this.value) == "") {
		this.value = this.defaultValue;
		$(this).addClass("input-in");
	}
});
