//选择行业
function chooseIndustry(index,id){
	var d = "";
	var title = $("#industries_"+index).text();
	var arrayList = industries[index].children;
	if('#currentlyIndustry'.trim()== id.trim()){
		$(".child-title").html("<li onclick=\"chooseIndudtries('"+industries[index].name+"','"+id+"')\">"+industries[index].name+"</li>");
		for(var i=0;i<arrayList.length;i++){
			d +="<li onclick=\"chooseIndudtries('"+arrayList[i].name+"','"+id+"')\">"+arrayList[i].name+"</li>"
		}
	}else{
		var isChecked="";
		if($("#poc_"+industries[index].id).attr("checked")=="checked"){
			isChecked = "checked";
		}
		$(".child-title").html("<li onclick='chooseCheckBoxIndudtries(&quot"+industries[index].name+"&quot,"+industries[index].id+")'><input type='checkbox' "+isChecked+" onclick=\"industryCheckboxCategory('"+industries[index].name+"','"+industries[index].id+"')\" class='industry_"+industries[index].id+"'/> "+industries[index].name+"</li>");
		for(var i=0;i<arrayList.length;i++){
			var cId = arrayList[i].id
			isChecked="";
			if($("#poc_"+cId).attr("checked")=="checked"){
				isChecked = "checked";
			}
			d +="<li onclick='chooseCheckBoxIndudtries(&quot"+arrayList[i].name+"&quot,"+cId+")'><input type='checkbox' "+isChecked+" onclick=\"industryCheckboxCategory('"+arrayList[i].name+"','"+cId+"')\" class='industry_"+cId+"'/> "+arrayList[i].name+"</li>"
		}
	}
	$(".child-content").html(d);
	$(".industry-child-data").show();
	var lt = $("#industries_"+index+" span").offset();
	var w = $("#industries_"+index+" span").width();
	$(".industry-child-data ").css({
		top:lt.top,
		left:lt.left+w,
		"z-index":"100000"
	});
}

