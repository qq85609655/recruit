//选择行业

function chooseCheckBoxIndudtries(name,cId){
    var d = "";
	var c=".industry_"+cId;
	if(industryNum<5){
		if($(c).attr("checked")){
			$(c).attr("checked",false);
			$("#industry_"+cId).remove();
			industryNum--;
		}else{
			$(c).attr("checked","checked");
			d += '<li class="hasChild" value="'+cId+'" id="industry_'+ cId +'" onclick="industryRemoveCategory(' + cId + ",'"
			 + name + '\')" title="' + name + '">' +'<input type="checkbox" id="poc_'+ cId +'" checked="checked" >'+ name +'</li>'
			$(".industry-data").append(d);
			industryNum++;
		}
	}else{
		if($(c).attr("checked")){
			$(".industry-prompt").empty();
			$(c).attr("checked",false);
			$("#industry_"+cId).remove();
			industryNum--;
		}else{
			$(c).removeAttr("checked"); 
			$(".industry-prompt").html("<font color='red'>不能多于五个</font>");
		}
	}
}

//行业复选框出去checkbox触发两次事件
function industryCheckboxCategory(name,cId){
	var c=".industry_"+cId;
	if($(c).attr("checked")){
		$(c).attr("checked",false);
	}else{
		$(c).attr("checked",true);
	}
}

//删除所选的行业
function industryRemoveCategory(cId,name){
	$("#industry_"+cId).remove();
	industryNum--;
	$(".industry_"+cId).attr("checked",false);
}

