
$("#position-info-button1").live("click",function() {
	$(this).toggleClass("button-out",false);
	$(this).toggleClass("button-up",true);
	$("#position-info-button2").toggleClass("button-out",true);
	$(".position-info2").show();
	
	
});

$("#position-info-button2").live("click",function() {
	$("#position-info-button1").toggleClass("button-out",true);
	$(this).toggleClass("button-out",false);
	$(this).toggleClass("button-up",true);
	$(".position-info2").hide();
});
