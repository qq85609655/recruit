package com.csot.gm.rawMaterial.model;

public class PartProperty {
  private String partId;
  private String partName;
  private String propertyId;
  private String propertyName;
  private String propertyDisName;
  private String propertyUnit;
  private String isUnique;
  private String isEnum;

  public String getPartId() {
    return partId;
  }

  public void setPartId(String partId) {
    this.partId = partId;
  }

  public String getPartName() {
    return partName;
  }

  public void setPartName(String partName) {
    this.partName = partName;
  }

  public String getPropertyId() {
    return propertyId;
  }

  public void setPropertyId(String propertyId) {
    this.propertyId = propertyId;
  }

  public String getPropertyName() {
    return propertyName;
  }

  public void setPropertyName(String propertyName) {
    this.propertyName = propertyName;
  }

  public String getPropertyDisName() {
    return propertyDisName;
  }

  public void setPropertyDisName(String propertyDisName) {
    this.propertyDisName = propertyDisName;
  }

  public String getPropertyUnit() {
    return propertyUnit;
  }

  public void setPropertyUnit(String propertyUnit) {
    this.propertyUnit = propertyUnit;
  }

  public String getIsUnique() {
    return isUnique;
  }

  public void setIsUnique(String isUnique) {
    this.isUnique = isUnique;
  }

  public String getIsEnum() {
    return isEnum;
  }

  public void setIsEnum(String isEnum) {
    this.isEnum = isEnum;
  }
}
