package com.csot.gm.rawMaterial.model;

public class EnumTypeValue {
  private String enumTypeName;
  private String enumValueText;
  private String enumValueValue;

  public String getEnumValueText() {
    return enumValueText;
  }

  public void setEnumValueText(String enumValueText) {
    this.enumValueText = enumValueText;
  }

  public String getEnumValueValue() {
    return enumValueValue;
  }

  public void setEnumValueValue(String enumValueValue) {
    this.enumValueValue = enumValueValue;
  }

  public String getEnumTypeName() {
    return enumTypeName;
  }

  public void setEnumTypeName(String enumTypeName) {
    this.enumTypeName = enumTypeName;
  }

}
